#!/bin/bash
	# Este es nuestro primer progrma
	echo Hola Mundo

# El comando ls es muy útil para ver los archivos y directorios 
# que tenemos dentro del directorio en el que estamos

$ ls 

# navegar el sistema de archivos desde la línea de comandos

$ pwd 

# CD (Change Directory) sirve para mostrar el nombre del directorio actual y, 
# también, permite cambiar de directorio. 

$ cd name_directory

# Imprima el directorio de trabajo nuevamente para ver la nueva ubicación.

$ cd ..

# Liste todos los archivos y directorios en el directorio de trabajo.

$ cd ../feb

#  mkdir tiene su origen en las palabras make subdirectory que significan crear subdirectorio en inglés.

$ mkdir media 

# El comando touch se usa principalmente para crear archivos vacíos y cambiar marcas de tiempo de archivos o carpetas

$ touch keyboard.txt